			<p>
				In the early 1900s, the city of Minneapolis continued industrializing with flour mills. Food processing
				plants were introduced and are still popular today. Also the Hennepin Island Hydroelectric Plant,
				created in 1906, now
				run by Xcel Energy, gave rise to more economic opportunities. Like other cities in the United States
				during the 1930s, Minneapolis&ndash;St. Paul was devastated by the Great Depression and was further
				affected by the
				following World Wars. <sup><a href="#history-source1">[1]</a></sup> The Twin Cities began recovering in
				the 1980s and 90s due to opportunities in construction, which allowed for more jobs and economic growth.
				This boom
				produced many of the business skyscrapers and residential buildings that are there today. <sup><a
						href="#history-source2">[2]</a></sup>
			</p>
