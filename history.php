			<h2>History</h2>
			<p>
				Minneapolis&ndash;St. Paul is a large metropolitan area separated by the Mississippi River. Founded
				first, Minneapolis was established by immigrants coming from Denmark, northern Germany, Norway, and
				Sweden in 1855. Setting
				up near St. Anthony Falls, pioneers used the Falls for powering sawmills and flour mills. By 1870,
				Minneapolis became one of the world's leading milling capitals for flour. While Minneapolis grew, St.
				Paul started taking
				shape on the opposite side of the river. St. Paul started from the two individual villages&mdash;Pig's
				Eye and Lambert's Landing. These villages, due to the nearby boat traffic, became harbors for trading.
				Minneapolis&ndash;St. Paul, connected by rail in 1867, began to expand significantly. <sup><a
						href="#history-source2">[2]</a></sup>
			</p>
